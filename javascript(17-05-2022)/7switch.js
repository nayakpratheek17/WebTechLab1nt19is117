colorCode = prompt("Enter the color code");

switch (colorCode) {
    case 'R' :
        document.write("RED");
        break;
    case 'G' :
        document.write("GREEN");
        break;
    case 'B' : 
        document.write("BLUE");
        break;
    case 'O' :
        document.write("ORANGE");
        break;
    case 'Y' :
        document.write("YELLOW");
        break;
    case 'W' :
        document.write("WHITE");
        break;
    default :
        document.write("Invalid");
}